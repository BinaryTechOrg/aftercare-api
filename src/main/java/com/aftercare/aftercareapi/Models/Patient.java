package com.aftercare.aftercareapi.Models;

public class Patient {
    public int id;
    public String name;
    public String lastName;

    public void setId(int id){
        this.id = id;
    }

    public void setName(String name){
        this.name = name;
    }

    public void setLastName(String lastName){
        this.lastName = lastName;
    }
}
